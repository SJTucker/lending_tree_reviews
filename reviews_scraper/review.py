class Review:
  def __init__(self, title, body, rating, author, location, date, closed_with_lender, loan_type, review_type):
    self.title = title
    self.body = body
    self.rating = rating
    self.author = author
    self.location = location
    self.date = date
    self.closed_with_lender = closed_with_lender
    self.loan_type = loan_type
    self.review_type = review_type